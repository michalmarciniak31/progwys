def pole_szescianu(a):
    return a**2*6

def objetosc_szescianu(a):
    return a**3

def pole_prostopadloscianu(a, b, h):
    return 2(a*b + a*h + b*h)

def objetosc_prostopadloscianu(a, b, h):
    return a*b*h

def pole_kuli(r):
    return 4*3.14*r**3

def objetosc_kuli(r):
    return 4/3*3.14*r**3